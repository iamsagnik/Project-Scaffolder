async function realistic(ctx = {}) {
  const name = ctx.projectName || "enterprise-monorepo";
  const content = `# ${name}

Monorepo scaffold (frontend + backend + shared packages)

## Workspace layout
- apps/frontend (React + Vite)
- apps/backend (Node + Express)
- packages/shared-*

## Quickstart
\`\`\`bash
pnpm install
pnpm -w dev
\`\`\`
`;
  return { type: "single", content };
}

async function minimal(ctx = {}) {
  const content = `# Monorepo
Generated by Project Scaffolder.`;
  return { type: "single", content };
}

async function enterprise(ctx = {}) {
  const name = ctx.projectName || "enterprise-monorepo";
  const content = `# ${name} (Enterprise)

This monorepo includes CI, workspace tooling, and production-ready Docker + deployment guidance.
See /docs for more details.
`;
  return { type: "single", content };
}

/* template-specific variant: "enterprise" */
async function enterpriseVariant(ctx = {}) {
  return enterprise(ctx);
}

async function defaultVariant(ctx = {}) {
  return realistic(ctx);
}

module.exports = {
  realistic,
  minimal,
  enterprise: enterpriseVariant,
  default: defaultVariant
};
